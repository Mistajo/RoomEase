    public function getReservationsByRoom()
    {
        $query = $this->createQueryBuilder('r')
            ->select('COUNT(r.id) as reservation_count, m.name as room_name')
            ->leftJoin('r.meetingroom', 'm')
            ->groupBy('m.id')
            ->getQuery();

        return $query->getResult();
    }
